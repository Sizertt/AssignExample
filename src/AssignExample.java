//Пример присваивания значений переменных
public class AssignExample {
    public static void main(String[] args) {

        //Обьявляем две переменные
        int var1;
        int var2;

       // Присваиваем им значения
        var1 = 4096;
        var2 = var1 / 4;

        // Выводим текущее значения переменных
        System.out.println("var1 = ”" + var1);
        System.out.println("var2 = var1 / 4 = " + var2);

    } // main()

} // AssignExample class



